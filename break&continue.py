#Break statement
for i in "Devops":
    print(i)
    if i == "o":
        print("i have found my data. ") 
        break #break statement
print("Out of loop")   


#Continue statement
for i in "Devops":
    if i == "o":
        print("i have found my data. ") 
        continue #continue statement
    print(i)
print("Out of loop")   


 